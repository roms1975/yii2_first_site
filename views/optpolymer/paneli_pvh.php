                <h2><a name="pvhlaminacia">Панели ПВХ с ламинацией</a></h2>
                <p>
                    Ламинированные панели изготавливаются путем приклеивания  плёнки с рисунком на панель (ламинация). Этот вид панелей не рекомендуется использовать во влажных помещениях, так как со временем под действием влаги слой ламинации может отойти от основания по краям панели. Но эти панели имеют своё преимущество - плёнка может иметь объемную текстуру (как на обоях). Это текстура может имитировать поверхность шелка, льна, холста и других материалов и объем чуствуется на ощупь. 
                </p>
                <p>
                    Еще одним интересным видом панелей с ламинацией являются панели с голографией. Плёнка используемая при изготовлении панели с ламинацией имеет голографический рисунок, то есть такой металлизированный оттенок который меняет свой цвет если смотреть на него  под разным углом.
                </p>
